    public void SelectProducts(){
        int c;
        try{
            SqlConnection sqlConnection = new SqlConnection();
            add = sqlConnection.con.prepareStatement("select * from items");
            Rs = add.executeQuery();
            
            ResultSetMetaData Rsd = Rs.getMetaData();
            c = Rsd.getColumnCount();
            
            DefaultTableModel d = (DefaultTableModel)ProductInfoTable.getModel();
            d.setRowCount(0);
            
            while(Rs.next()){
                Vector v = new Vector();
                
                for(int i=1; i<=c; i++){
                    v.add(Rs.getInt("item_id"));
                    v.add(Rs.getString("name"));
                    v.add(Rs.getString("main_cat"));
                    v.add(Rs.getString("cat_name"));
                    v.add(Rs.getString("desc"));
                    v.add(Rs.getInt("qty_s"));
                    v.add(Rs.getInt("price_s"));
                    v.add(Rs.getInt("qty_m"));
                    v.add(Rs.getInt("price_m"));
                    v.add(Rs.getInt("qty_l"));
                    v.add(Rs.getInt("price_l"));
                    v.add(Rs.getString("img1"));
                    v.add(Rs.getString("img2"));
                    v.add(Rs.getString("img3"));
                }
                d.addRow(v);
            }
            
        }catch(SQLException e){
            e.printStackTrace();
        }
    }
    
    private void GetCategory(){
        try{
            SqlConnection sqlConnection = new SqlConnection();
            St = sqlConnection.con.createStatement();
            String query = "select DISTINCT(name) from category";
            Rs = St.executeQuery(query);
            
            while(Rs.next()){
                String category = Rs.getString("name");
                SubCategoryComboBox.addItem(category);
            }

            
        }catch(SQLException e){
            e.printStackTrace();
        }
    }
    
